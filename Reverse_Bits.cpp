class Solution {
  public:
    long long reversedBits(long long X) {
        long long reverse =0;
        
        int n=1;
        while(n<=32){
           int remain = X%2;
           reverse = reverse*2+remain;
           X/=2;
           n++;
        }
        return reverse;
    }
};

